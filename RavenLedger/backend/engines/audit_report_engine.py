def save_report(case: dict) -> Path:
    REPORTS_DIR.mkdir(parents=True, exist_ok=True)

    case_id = case.get("correlation_case_id", "unknown-case")
    report_path = REPORTS_DIR / f"{case_id}.md"

    report = generate_markdown_report(case)

    with open(report_path, "w", encoding="utf-8") as file:
        file.write(report)

    return report_path


def generate_reports(limit: int | None = None) -> list[Path]:
    cases = load_policy_cases()

    if limit is not None:
        cases = cases[:limit]

    report_paths = []

    for case in cases:
        path = save_report(case)
        report_paths.append(path)

    return report_paths


if __name__ == "__main__":
    report_paths = generate_reports(limit=10)

    print("RavenLedger Audit Report Engine")
    print("=" * 70)

    for path in report_paths:
        print("Generated:", path)

    print("\nTotal reports generated:", len(report_paths))