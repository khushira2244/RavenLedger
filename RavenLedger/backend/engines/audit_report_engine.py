from pathlib import Path
import sys
import json

ROOT_DIR = Path(__file__).resolve().parents[2]
sys.path.append(str(ROOT_DIR))

POLICY_CASES_PATH = ROOT_DIR / "data" / "processed" / "ravenledger_policy_cases.json"
REPORTS_DIR = ROOT_DIR / "data" / "processed" / "reports"


def load_policy_cases() -> list[dict]:
    if not POLICY_CASES_PATH.exists():
        raise FileNotFoundError(
            f"Missing file: {POLICY_CASES_PATH}. Run policy_engine.py first."
        )

    with open(POLICY_CASES_PATH, "r", encoding="utf-8") as file:
        return json.load(file)


def bullet_list(items) -> str:
    if not items:
        return "- None recorded"

    return "\n".join(f"- {item}" for item in items)


def build_splunk_evidence_section(case: dict) -> str:
    evidence = case.get("splunk_evidence_sample", [])

    if not evidence:
        return "- No Splunk evidence attached."

    lines = []

    for item in evidence[:5]:
        time_value = item.get("time", "")
        host = item.get("host", "")
        sourcetype = item.get("sourcetype", "")
        source = item.get("source", "")

        lines.append(
            f"- `{time_value}` | host=`{host}` | sourcetype=`{sourcetype}` | source=`{source}`"
        )

    return "\n".join(lines)


